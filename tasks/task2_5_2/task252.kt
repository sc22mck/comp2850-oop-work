fun main() {
    val myAge = 29u
    println(myAge::class)
    val universeAge = 13_800_000_0001
    println(universeAge::class)
    val status = 'M'
    println(status::class)
    val name = "Sarah"
    println(name::class)
    val height = 1.78f
    println(name::height)
    val root2 = Math.sqrt(2.0)
    println(root2::class)
}